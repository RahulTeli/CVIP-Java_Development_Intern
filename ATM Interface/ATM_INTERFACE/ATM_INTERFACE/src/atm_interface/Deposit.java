    private void EnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterActionPerformed
      
        if("".equals(amount.getText().trim()))
        {
            JOptionPane.showMessageDialog(null, "Field are Empty","Message",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            try
            {
                Connection con = ConnectionProvider.getcon();
                String query = "update Customer set Balance = ? where Card = ?";
                PreparedStatement st = con.prepareStatement(query);
                st.setLong(1, getBalance() + Long.parseLong(amount.getText().trim()));
                st.setInt(2,cardno);   
                
               if(st.executeUpdate()==1)
               {
                   JOptionPane.showMessageDialog(null, "Balance Updated","Deposit",JOptionPane.INFORMATION_MESSAGE);
                   transac();
                   this.dispose();
                   new Process().setVisible(true);
               }
            }
            catch(Exception e)
            {
                  JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_EnterActionPerformed

