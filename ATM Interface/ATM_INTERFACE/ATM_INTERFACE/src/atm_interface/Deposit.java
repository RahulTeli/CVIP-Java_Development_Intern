    long Oldbalance;
    private long getBalance()
    {
        
        try
           {
                Connection con = ConnectionProvider.getcon();
                Statement st = con.createStatement();
                String query = "select * from Customer where Card= '"+cardno+"'";
                ResultSet rs = st.executeQuery(query);
                
                 if(rs.next())
                {
                    Oldbalance = rs.getLong(4);
                    
                }
                
                
                
           }
           catch(Exception e)
           {
               JOptionPane.showMessageDialog(null, "Error" ,"Error" , JOptionPane.ERROR);
           }
        return Oldbalance;
    }
    String date ;
    private String getDate()
    {
        Date d = new Date();
        SimpleDateFormat sf = new SimpleDateFormat("dd-MM-yyyy");
        date = sf.format(d);
        return date;
        
        
    }
    private void transac()
    {
        try
        {
            Connection con = ConnectionProvider.getcon();
            String query = " insert into transaction (Card_No,Type,Amount,Date) values (?,?,?,?)";
            PreparedStatement st = con.prepareStatement(query);
            st.setInt(1, cardno);
            st.setString(2, "Deposit");
            st.setLong(3, Long.parseLong(amount.getText().trim()));
            st.setString(4,getDate() );
            
            st.executeUpdate();
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
       
