    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

        if ("".equals(Name.getText()) || "".equals(Contact.getText()) || "".equals(Add.getText())
                || "".equals(courseName.getText()) || "".equals(fees.getText()) || "".equals(paidfees.getText())
                || "".equals(duefees.getText())) {
            JOptionPane.showMessageDialog(null, "Enter Details", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!"".equals(Name.getText()) && !"".equals(Contact.getText()) && !"".equals(Add.getText())
                && !"".equals(courseName.getText()) && !"".equals(fees.getText()) && !"".equals(paidfees.getText())
                && !"".equals(duefees.getText())) {
            String gender = (String) Gen.getSelectedItem();
            try {
                Connection Con = ConnectionProvider.getcon();
                Statement st = Con.createStatement();
                String query = "insert into student values('" + Name.getText().trim() + "' , '" + Contact.getText().trim() + "' , '" + Email.getText().trim() + "' , '" + Add.getText().trim() + "' , '" + gender + "',"
                        + "'" + courseName.getText().trim() + "','" + fees.getText().trim() + "','" + paidfees.getText().trim() + "','" + duefees.getText().trim() + "' ) ";
                st.executeUpdate(query);

                JOptionPane.showMessageDialog(null, "Student Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                Reset();
                fetch();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Mobile Number is already Exists.");
            }
        }

    }//GEN-LAST:event_saveActionPerformed


    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String gender = (String) Gen.getSelectedItem();
        try {
            Connection con = ConnectionProvider.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("update student set Name='" + Name.getText().trim() + "' , Contact='" + Contact.getText().trim() + "' , Email='" + Email.getText().trim() + "', Address='" + Add.getText().trim() + "', Gender='" + gender + "'"
                    + ", Course = '" + courseName.getText().trim() + "', Fees= '" + fees.getText().trim() + "', Paid = '" + paidfees.getText().trim() + "' , Due = '" + duefees.getText().trim() + "'   where Contact='" + Contact.getText().trim() + "'   ");

            JOptionPane.showMessageDialog(null, "Student Updated", "Success", JOptionPane.INFORMATION_MESSAGE);

            Reset();
            fetch();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_updateActionPerformed

