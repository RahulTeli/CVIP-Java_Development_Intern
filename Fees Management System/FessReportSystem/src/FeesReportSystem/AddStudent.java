    private void ContactKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ContactKeyReleased
        String mob = Contact.getText().trim();
        if (mob.matches("[7-9][0-9]{9}")) {
            Contact.setForeground(Color.black);
        } else {
            Contact.setForeground(Color.red);
        }
    }//GEN-LAST:event_ContactKeyReleased

    private void EmailKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_EmailKeyReleased
        String em = Email.getText().trim();
        if (em.matches("[a-zA-Z0-9][a-zA-Z0-9_.]+@[a-zA-z0-9]+([.][a-zA-Z]+)+")) {
            Email.setForeground(Color.black);
        } else {
            Email.setForeground(Color.red);
        }
    }//GEN-LAST:event_EmailKeyReleased

