    public void pidl() {
        try {
            Connection con = ConnectionProvider.getcon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select max(Course_ID) from course");
            if (rs.next()) {
                int id = rs.getInt(1);
                id = id + 1;
                String str = String.valueOf(id);
                lab.setText(str);
            } else {
                lab.setText("1");
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
    }
