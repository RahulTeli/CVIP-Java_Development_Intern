    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

        if ("".equals(CName.getText()) || "".equals(CPrice.getText()) || "".equals(CDesc.getText())) {
            JOptionPane.showMessageDialog(null, "Enter Valid Details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!"".equals(CName.getText()) && !"".equals(CPrice.getText()) && !"".equals(CDesc.getText())) {
           

            try {
                Connection con = ConnectionProvider.getcon();
                Statement st = con.createStatement();
                String query = "insert into course values('" + lab.getText().trim() + "','" + CName.getText().trim() + "' , '" + CPrice.getText().trim() + "' , '" + CDesc.getText().trim() + "'  ) ";
                st.executeUpdate(query);

                JOptionPane.showMessageDialog(null, "Course Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                pidl();
                Reset();
                fetch();
                
            } catch (Exception e) {
                //JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_saveActionPerformed
